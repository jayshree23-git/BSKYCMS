package com.project.bsky.bean;

public class WardBean {
    private String wardName;
    private Integer wardMasterId;
    private String implantCode;

    public String getWardName() {
        return wardName;
    }

    public void setWardName(String wardName) {
        this.wardName = wardName;
    }

    public Integer getWardMasterId() { return wardMasterId; }

    public void setWardMasterId(Integer wardMasterId) { this.wardMasterId = wardMasterId; }

    public String getImplantCode() {
        return implantCode;
    }

    public void setImplantCode(String implantCode) {
        this.implantCode = implantCode;
    }
}
